    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        String oldPW = oldPwVal.getText(); 
        String newPW = newPwVal.getText();
        String cfmPW = cfmPwVal.getText();
        String fullName = fullNameVal.getText();
        String email = emailVal.getText();
        String phone = phoneVal.getText();
        // prevent empty fields from happening
        if(checkEmptyField(oldPW, newPW, cfmPW, fullName, email, phone))
            JOptionPane.showMessageDialog (null, "All fields are empty!\nNo update is done.");
        else
        {
            //Validate password field
            if(!oldPW.isEmpty() && s.getPassword().equals(oldPW))
            {
                if(!newPW.equals(cfmPW) || oldPW.equals(newPW))
                {
                    JOptionPane.showMessageDialog (null, "Failed to save information:\n"
                                                          + "Either old and new password is the same or new and confirm password does not match");
                }
                else 
                    validateField(fullName, email, phone, cfmPW);
            }
            else if(oldPW.isEmpty() || newPW.isEmpty() || cfmPW.isEmpty())
                validateField(fullName, email, phone, "");
            else
                JOptionPane.showMessageDialog (null, "Old password does not match");
        }
        setFieldsEmpty();
      
    }//GEN-LAST:event_saveButtonActionPerformed

