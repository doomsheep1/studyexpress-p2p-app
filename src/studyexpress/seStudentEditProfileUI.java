    public void setUpdate(String pw, String fullName, String email, String phone)
    {
        pw = !pw.isEmpty()?" password='" + pw:"";

        fullName = !fullName.isEmpty()?" fullname='" + fullName:"";

        email = !email.isEmpty()?" email='" + email:"";

        phone = !phone.isEmpty()?" phone='" + phone:"";

        StudentEditProfileController.updateProfile(s, pw, fullName, email, phone);
    }
    
